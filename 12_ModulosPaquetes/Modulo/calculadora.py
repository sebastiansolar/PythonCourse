

def sumar(op1,op2):
    suma = op1 + op2
    print(f'El resultado de tu suma es: {suma}')

def restar(op1,op2):
    restar = op1 - op2
    print(f'El resultado de tu suma es: {restar}')

def dividir(op1,op2):
    suma = op1 / op2
    print(f'El resultado de tu suma es: {suma}')

def multiplicar(op1,op2):
    suma = op1*op2
    print(f'El resultado de tu suma es: {suma}')